#include "fractol.h"

int		color(t_env *env)
{
	int		r;
	int		g;
	int		b;
	int		color;

	r = (255 / env->r);
	g = (255 / env->g);
	b = (255 / env->b);
	color = b << 16 | g << 8 | r;
	return (color * env->i);
}
